package env_vars

import (
	"fmt"
	"os"
	"strings"

	"github.com/ironstar-io/ironstar-cli/cmd/flags"
	"github.com/ironstar-io/ironstar-cli/internal/api"
	"github.com/ironstar-io/ironstar-cli/internal/env_vars"
	"github.com/ironstar-io/ironstar-cli/internal/system/utils"

	"github.com/fatih/color"
	"github.com/spf13/cobra"
)

// ModifyCmd - `iron env-vars modify`
var ModifyCmd = &cobra.Command{
	Use:   "modify",
	Short: "Modify an environment variable",
	Long:  "Modify an environment variable in an Ironstar environment",
	Run:   modify,
}

func modify(cmd *cobra.Command, args []string) {
	err := env_vars.Modify(args, flags.Acc)
	if err != nil {
		if err != api.ErrIronstarAPICall {
			if strings.ToLower(flags.Acc.Output) == "json" {
				utils.PrintErrorJSON(err)
				os.Exit(1)
			}

			fmt.Println()
			color.Red(err.Error())
		}

		os.Exit(1)
	}
}
